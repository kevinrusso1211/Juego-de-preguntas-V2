    public boolean verificarSiExistePregunta(String pregunta){
        boolean r = false;
        for(int i = 0; i < crud.obtenerPreguntas().size(); i ++){
            if(pregunta.equals(crud.obtenerPreguntas().get(i).pregunta)){
                r=true;
                break;
            }
        }
        return r;
    } 
    
