    private void btnGuardarPreguntaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarPreguntaActionPerformed
        // TODO add your handling code here:
        if(verificarSiExistePregunta(this.txtPregunta.getText())){
            JOptionPane.showMessageDialog(null, "Ya esta pregunta existe");
            txtPregunta.setText("");
            txtA.setText("");
            txtB.setText("");
            txtC.setText("");
        }else{
            pre = txtPregunta.getText();
            a = txtA.getText();
            b = txtB.getText();
            c = txtC.getText();
            oc = String.valueOf(txtRespuesta.getSelectedItem());
            pregunta = new Pregunta(pre,a,b,c,oc);
            crud.crear(pregunta);

            txtPregunta.setText("");
            txtA.setText("");
            txtB.setText("");
            txtC.setText("");

            JOptionPane.showMessageDialog(null, "Guardado");
        }
    }//GEN-LAST:event_btnGuardarPreguntaActionPerformed

